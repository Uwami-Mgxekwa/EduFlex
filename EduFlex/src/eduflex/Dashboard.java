    // Method to update JLabel text dynamically with unique activities
public void updateLabelsWithTimer() {
    // List of random activities for each label
    String[] activities1 = {
        "Completed Assignment", "Joined Group Discussion",
        "Submitted Quiz", "Started New Project",
        "Uploaded Study Notes", "Marked Attendance",
        "Viewed Lesson", "Downloaded Resource",
        "Logged into Portal", "Updated Profile"
    };

    String[] activities2 = {
        "Watched Tutorial", "Read an Article", 
        "Created Presentation", "Shared Notes", 
        "Signed Up for Course", "Took a Practice Test", 
        "Checked Notifications", "Updated Calendar", 
        "Generated Report", "Searched Online"
    };

    String[] activities3 = {
        "Participated in Poll", "Sent Message", 
        "Organized Group Chat", "Booked Meeting Room",
        "Added Class Schedule", "Published Homework",
        "Recorded Video", "Commented on Post", 
        "Started Brainstorming", "Prepared for Quiz"
    };

    Random random = new Random();

    // Timer to update activities every 5 minutes (300,000 milliseconds)
    Timer timer = new Timer(30000, e -> {
        // Set random text for each JLabel
        act1.setText(activities1[random.nextInt(activities1.length)]);
        act3.setText(activities2[random.nextInt(activities2.length)]);
        act5.setText(activities3[random.nextInt(activities3.length)]);
    });

    timer.start(); // Start the timer
}

